// VARIÁVEIS GLOBAIS
let redacaoAtual = null;

// FUNÇÃO PARA MOSTRAR REDAÇÃO
function mostrarRedacao(numero) {
    console.log('Clicou no botão! Número:', numero); // DEBUG
    redacaoAtual = numero;
    const modal = document.getElementById('modal');
    const titulo = document.getElementById('modalTitle');
    const conteudo = document.getElementById('conteudo-redacao');
    
    if (redacoes[numero]) {
        titulo.textContent = redacoes[numero].titulo;
        conteudo.innerHTML = `
            <p><strong>Tema:</strong> ${redacoes[numero].tema}</p>
            ${redacoes[numero].texto}
        `;
        modal.style.display = 'flex';
        document.body.style.overflow = 'hidden';
    } else {
        console.log('Redação não encontrada:', numero); // DEBUG
    }
}

// FUNÇÃO PARA FECHAR MODAL
function fecharModal() {
    const modal = document.getElementById('modal');
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';
}

// FUNÇÃO REDAÇÃO ANTERIOR
function redacaoAnterior() {
    if (redacaoAtual > 1) {
        mostrarRedacao(redacaoAtual - 1);
    } else {
        mostrarRedacao(15);
    }
}

// FUNÇÃO PRÓXIMA REDAÇÃO
function proximaRedacao() {
    if (redacaoAtual < 15) {
        mostrarRedacao(redacaoAtual + 1);
    } else {
        mostrarRedacao(1);
    }
}

// EVENT LISTENERS - AGORA VAI FUNCIONAR, PORRA!
document.addEventListener('DOMContentLoaded', function() {
    console.log('Página carregada! JavaScript funcionando!'); // DEBUG
    
    // Fechar modal clicando fora
    window.addEventListener('click', function(event) {
        const modal = document.getElementById('modal');
        if (event.target === modal) {
            fecharModal();
        }
    });

    // Fechar modal com ESC e navegar com setas
    document.addEventListener('keydown', function(event) {
        const modal = document.getElementById('modal');
        if (modal.style.display === 'flex') {
            if (event.key === 'Escape') {
                fecharModal();
            }
            if (event.key === 'ArrowLeft') {
                redacaoAnterior();
            }
            if (event.key === 'ArrowRight') {
                proximaRedacao();
            }
        }
    });

    // Debug: verificar se os botões estão funcionando
    document.querySelectorAll('.btn-ler').forEach((botao, index) => {
        botao.addEventListener('click', function() {
            console.log('Botão clicado:', index + 1);
        });
    });
});

